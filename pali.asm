ASSUME CS:CODE,DS:DATA
DATA SEGMENT
 A DB "NIVIM$"
 COUNT EQU 0005H
 MSG1 DB "PALINDROME$"
 MSG2 DB "NOT PALINDROME$"
 S1 DB "$"
 B DB "$"
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV CX,COUNT
MOV SI,OFFSET A
BACK:
INC SI
LOOP BACK
DEC SI

MOV CX,COUNT
MOV DI,OFFSET B
L1:
MOV AL,[SI]
MOV [DI],AL
INC DI
DEC SI
LOOP L1

MOV AL,S1
MOV [DI],AL

MOV CX,COUNT
MOV SI,OFFSET A
MOV DI,OFFSET B
L2:
MOV AL,[SI]
MOV BL,[DI]
CMP AL,BL
JNZ L3
INC SI
INC DI
LOOP L2

L3:
CMP CX,0000H
JZ L4
 MOV DX,OFFSET MSG2
 MOV AH,09H
 INT 21H 
 JMP L5
L4:
 MOV DX,OFFSET MSG1
 MOV AH,09H
 INT 21H 
L5: 
MOV AH,4CH
INT 21H
CODE ENDS
END START
