ASSUME CS:CODE,DS:DATA
DATA SEGMENT
 A DB 02H,05H,06H,78H,65H
 L DB 00H
 S DB 00H
 COUNT EQU 00005H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV CX,COUNT 
MOV SI,OFFSET A
MOV BL,00H
L1:
MOV AL,[SI]
CMP AL,BL
JC L2
  MOV BL,[SI]
L2:
INC SI 
DEC CX
JNZ L1
MOV DI,OFFSET L
MOV [DI],BL

MOV CX,COUNT 
MOV SI,OFFSET A
L3:
MOV AL,[SI]
CMP AL,BL
JNC L4
  MOV BL,[SI]
L4:
INC SI 
DEC CX
JNZ L3
MOV DI,OFFSET S
MOV [DI],BL

MOV AH,4CH
INT 21H
CODE ENDS
END START 
 
