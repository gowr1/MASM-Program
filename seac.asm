 B DB "L$"
 MSG1 DB "FOUND$"
 MSG2 DB "NOT FOUND$"
 COUNT DW 0000H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
MOV CX,0005H
MOV BX,0000H
MOV SI,OFFSET A
MOV AL,B
L1:
CMP  AL,[SI]
JNZ L2
 INC BX
L2: 
 INC SI
 DEC CX
 JNZ L1
MOV COUNT,BX 
CMP BX,0000H
JNZ L3
 MOV DX,OFFSET MSG2
 MOV AH,09H
 INT 21H
  JMP L4
L3:
 MOV DX,OFFSET MSG1
 MOV AH,09H
 INT 21H 
L4:
 MOV AH,4CH
 INT 21H
CODE ENDS
END START  
  
