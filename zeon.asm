ASSUME CS:CODE,DS:DATA
DATA SEGMENT
 A DB "11001011$"
 B DB "0$"
 COUNT EQU 0008H
 Z DB 00H
 O DB 00H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV CX,COUNT
MOV SI,OFFSET A
MOV BL,00H
MOV DL,00H
L1:
MOV AL,[SI]
CMP AL,B
JNZ L2 
 INC BL
 JMP L3
L2:
 INC DL
L3: 
INC SI
DEC CX
JNZ L1

MOV DI,OFFSET Z
MOV [DI],BL
MOV DI,OFFSET O
MOV [DI],DL

MOV AH,4CH
INT 21H
CODE ENDS
END START

 
