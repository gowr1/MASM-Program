ASSUME CS:CODE,DS:DATA
DATA SEGMENT
 A DB "STRING$"
 S1 DB "$"
 COUNT EQU 0006H
 B DB "$"
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV CX,COUNT
MOV SI,OFFSET A
BACK:
INC SI
LOOP BACK
DEC SI

MOV CX,COUNT
MOV DI,OFFSET B
L1:
MOV AL,[SI]
MOV [DI],AL
INC DI
DEC SI
LOOP L1

MOV AL,S1
MOV [DI],AL

MOV DX,OFFSET B
MOV AH,09H
INT 21H

MOV AH,4CH
INT 21H
CODE ENDS
END START
