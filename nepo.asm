ASSUME CS:CODE,DS:DATA
DATA SEGMENT
 A DB 02H,-05H,-06H,78H,65H
 P DB 05 DUP(?)
 COUNT EQU 00005H
DATA ENDS

CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX

MOV CX,COUNT 
MOV SI,OFFSET A
MOV DI,OFFSET P
L1:
MOV AL,[SI]
SHL AL,01H
JC L2
 MOV BL,[SI]
 MOV [DI],BL
 INC DI
L2:
INC SI
DEC CX
JNZ L1

MOV AH,4CH
INT 21H
CODE ENDS
END START 
 
